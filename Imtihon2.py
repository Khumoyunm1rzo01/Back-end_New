kun = str(input("Hafta kunini kiriting: "))

if kun == "Du":
    print("Dushanba")
elif kun == "Se":
    print("Seshanba")
elif kun == "Ch":
    print("Chorshanba")
elif kun == "Pa":
    print("Payshanba")
elif kun == "Ju":
    print("Juma")
elif kun == "Sh":
    print("Shanba")
elif kun == "Ya":
    print("Yakshanba")
else:
    print("Bunday kun mavjud emas!")